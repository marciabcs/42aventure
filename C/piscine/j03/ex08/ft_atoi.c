int	ft_atoi(char *str)
{
	int		i;
	int		n;
	int		neg;

	i = 0;
	n = 0;
	neg = 1;
	if (str[0] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[0] == '+')
		i++;
	while (str[i] != '\0')
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	n = n * neg;
	return (n);
}
