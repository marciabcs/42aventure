int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n)
	{
		if (*(s2 + i) != *(s1 + i))
			return (*(s1 + i) - *(s2 + i));
		else if (!*(s1 + i) || !*(s2 + i))
			return (*(s1 + i - 1) - *(s2 + i - 1));
		i++;
	}
	return (*(s1 + n - 1) - *(s2 + n - 1));
}
