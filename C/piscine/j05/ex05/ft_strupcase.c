char	*ft_strupcase(char *str)
{
	int		i;
	char	*temp;

	i = 0;
	temp = str;
	while (*(str + i))
	{
		if ((*(str + i) >= 'a') && (*(str + i) <= 'z'))
			*(temp + i) = *(str + i) - 32;
		i++;
	}
	return (temp);
}
