int	ft_str_is_alpha(char *str)
{
	int		i;
	char	s;

	i = 0;
	if (!*str)
		return (1);
	while (*(str + i))
	{
		s = *(str + i);
		if (!((s >= 'a' && s <= 'z') || (s >= 'A' && s <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}
