#include "def_ex06.h"

long int	ft_add(long int a, long int b)
{
	return (a + b);
}

long int	ft_sub(long int a, long int b)
{
	return (a - b);
}

long int	ft_mul(long int a, long int b)
{
	return (a * b);
}

long int	ft_div(long int a, long int b)
{
	return (a / b);
}

long int	ft_mod(long int a, long int b)
{
	return (a % b);
}
