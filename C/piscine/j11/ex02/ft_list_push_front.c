#include "ft_list.h"
#include <stdlib.h>

void	ft_list_push_front(t_list **begin_list, void *data)
{
	t_list	*aux;

	aux = ft_create_elem(data);
	aux->next = *begin_list;
	*begin_list = aux;
}
